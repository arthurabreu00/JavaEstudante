package Questoes;

import java.util.Scanner;

public class Questao06 {

	public static void main(String[] args) {
		
		Scanner input = new Scanner(System.in);
		System.out.print("Digite um número: ");
		short num =  input.nextShort();
		int i = 0;
		while (i < num){
			
			i++;
			
			System.out.println(i);
			
		}
	

	}

	
}
